const express = require('express');
const router = express.Router();
const multer = require('multer');
const { createServiceProviderProfile } = require('../controllers/profileController');
const path = require('path'); // ðŸ‘ˆ ADD THIS

// Configure multer for file uploads
const storage = multer.diskStorage({
  destination: function (req, file, cb) {
    cb(null, 'uploads/') // Make sure this directory exists
  },
  filename: function (req, file, cb) {
    const uniqueSuffix = Date.now() + '-' + Math.round(Math.random() * 1E9);
    cb(null, file.fieldname + '-' + uniqueSuffix + path.extname(file.originalname));
  }
});

// File filter to only accept images
const fileFilter = (req, file, cb) => {
  if (file.mimetype.startsWith('image/')) {
    cb(null, true);
  } else {
    cb(new Error('Only image files are allowed!'), false);
  }
};

const upload = multer({ 
  storage: storage,
  fileFilter: fileFilter,
  limits: {
    fileSize: 5 * 1024 * 1024 // 5MB limit
  }
});

// Profile creation route with file upload middleware
router.post('/create', 
  upload.single('profile_picture'), // Handle single file upload
  createServiceProviderProfile
);

module.exports = router;